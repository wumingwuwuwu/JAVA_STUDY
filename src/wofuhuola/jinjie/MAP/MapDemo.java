package wofuhuola.jinjie.MAP;

import java.util.Collection;
import java.util.HashMap;
import java.util.Map;
import java.util.Map.Entry;
import java.util.Set;
import java.util.function.BiConsumer;

/**
 * map的特性都是绑定的是key
 * 需求：我们的学生是有学号和姓名等信息的。
 * it001	林青霞
 * it002	王祖贤
 * it003	张曼玉
 * 用我们目前所学的知识，可以实现。
 * 把这两个作为一个类的成员变量，然后用Collection体系的集合存储对象即可。
 * 但是呢，我想根据学号直接就能找到人，如何实现呢?也是可以实现的，遍历的时候，依次比较学号即可。然后学号正确的，获取该对象的姓名变量。
 * 虽然是可以的，但是有点点麻烦，因为我们不可能每次要找一个人，都重头遍历一遍。
 * 假如有一种集合，能够实现学号和姓名的对应关系，我们就能简单很多了。
 * 一个学号对应着一个姓名，然后根据学号就能知道该姓名。为了满足这样的需求，java就提供了一个新的集合：Map。
 *
 * Map：元素是按照键值对形式存储的。每一对元素由两部分组成。分别叫键和值
 * 	          键是唯一的，值是可以重复的。
 * 	         所以Map集合的底层数据结构是针对键有效，跟值无关。
 *
 * Map接口和Collection接口的不同?(面试题)
 * 		A:Map集合是双列集合；Map集合的键是唯一的，值是可以重复的。其实我们也可以简单的理解为Map集合的键和值是由Set和List组成；数据结构针对键有效。
 * 		B:Collection集合是单列集合；Collection集合的儿子Set是唯一的，List是可以重复的；数据结构针对元素有效。
 *
 * Map集合的功能：
 * 		A:添加功能
 * 			V put(K key,V value):添加，替换或者修改。键不同，添加到集合。键相同，值替换。
 * 		B:移除功能
 * 			void clear():移除所有映射关系
 * 			V remove(Object key):根据键移除键值对元素，返回的是键对应的值
 * 		C:判断功能
 * 			boolean containsKey(Object key):判断Map集合中是否包含指定的键
 * 			boolean containsValue(Object value):判断Map集合中是否包含指定的值
 * 			boolean isEmpty():判断集合是否为空
 * 		D:获取功能
 * 			Set<Map.Entry<K,V>> entrySet():返回的是键值对对象的Set集合。
 * 			V get(Object key):根据键获取值
 * 			Set<K> keySet():所有的键的集合
 * 			Collection<V> values():所有值的集合
 * 			int size():集合的长度
 */
public class MapDemo {
    public static void main(String[] args) {
        // 创建集合对象
        // 创建元素
        // 添加元素
        // 遍历集合

        // 创建集合对象
        Map<String, String> map = new HashMap<String, String>();

        // 添加元素
        // V put(K key,V value)
        // System.out.println("put:" + map.put("文章", "马伊俐"));
        // System.out.println("put:" + map.put("文章", "姚笛"));
        map.put("文章", "马伊俐");
        map.put("谢霆锋", "张柏芝");
        map.put("田亮", "叶一茜");
        map.put("汪峰", "章子怡");

        // void clear():移除所有映射关系
        // map.clear();

        // V remove(Object key):根据键移除键值对元素，返回的是键对应的值
        // System.out.println("remove:" + map.remove("汪峰"));
        // System.out.println("remove:" + map.remove("汪峰2"));

        // boolean containsKey(Object key):判断Map集合中是否包含指定的键
        System.out.println("containsKey:" + map.containsKey("田亮"));
        System.out.println("containsValue:" + map.containsValue("章子怡"));

        // boolean isEmpty():判断集合是否为空
        System.out.println("isEmpty:" + map.isEmpty());

        // int size():集合的长度
        System.out.println("size:" + map.size());

        System.out.println("map:" + map);

        // V get(Object key)根据键获取值
        System.out.println("get:" + map.get("文章"));
        System.out.println("get:" + map.get("田亮"));
        System.out.println("---------------------");

        // Set<K> keySet()所有的键的集合
        Set<String> set = map.keySet();
        for (String key : set) {
            System.out.println(key);
        }
        System.out.println("---------------------");

        // Collection<V> values()所有值的集合
        Collection<String> cons = map.values();
        for (String value : cons) {
            System.out.println(value);
        }

        // 遍历
        // 获取Map集合的所有的键的Set集合
        Set<String> set2 = map.keySet();
        // 遍历键的Set集合，获取到每一个键
        for (String key : set) {
            // 根据键到Map集合中找对应的值
            String value = map.get(key);
            System.out.println(key + "---" + value);
        }

        // 遍历
        // 获取所有的键值对对象的Set集合。
        Set<Map.Entry<String, String>> entries = map.entrySet();
        // 遍历键值对对象的Set集合，得到每一个键值对对象。
        for (Map.Entry<String, String> me : entries) {
            // 根据键值对对象去获取键和值。getKey(),getValue()
            String key = me.getKey();
            String value = me.getValue();
            System.out.println(key + "---" + value);
        }
        System.out.println("________________________________________________");
        entries.forEach(o -> System.out.println(o.getKey() + "---" + o.getValue()));
        System.out.println("________________________________________________");

        // 获取所有的键值对对象的Set集合。
        //这里只要上面导入Map.Entry前面就不用加Map.了
        Set<Entry<String, String>> set1 = map.entrySet();
        // 遍历键值对对象的Set集合，得到每一个键值对对象。
        for (Entry<String, String> me : set1) {
            // 根据键值对对象去获取键和值。getKey(),getValue()
            String key = me.getKey();
            String value = me.getValue();
            System.out.println(key + "---" + value);
        }

        map.forEach(new BiConsumer<String, String>() {
            @Override
            public void accept(String key, String value) {
                System.out.println(key + "___" + value);
            }
        });

        map.forEach((key , value) -> System.out.println(key + "___" + value));

    }
}
